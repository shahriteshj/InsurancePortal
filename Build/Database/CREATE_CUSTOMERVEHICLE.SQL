CREATE TABLE CUSTOMERVEHICLE (
VEHICLEID NUMBER(10) PRIMARY KEY,
CUSTOMERID NUMBER(10) NOT NULL,
VEHICLEREGNO VARCHAR2(20),
MAKE VARCHAR2(50) NOT NULL,
MODEL VARCHAR2(50) NOT NULL,
SUBMODEL VARCHAR2(50),
ENGINENO VARCHAR2(20) NOT NULL,
CHASISNO VARCHAR2(20) NOT NULL,
CC VARCHAR2(10),
FUELTYPE VARCHAR2(20),
MANUFACTURINGYEAR NUMBER(4),
REGISTRATIONDATE DATE,
VEHICLEREGCITY VARCHAR2(50)
);