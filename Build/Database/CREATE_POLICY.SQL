
CREATE OR REPLACE TABLE POLICY (
POLICYID NUMBER(10) PRIMARY KEY,
CUSTOMERID NUMBER(10) NOT NULL,
VEHICLEID NUMBER(10) NOT NULL,
POLICYSTARTDATE DATE,
POLICYENDDATE DATE
);

