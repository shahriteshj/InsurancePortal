CREATE TABLE USERS (
	USERID NUMBER(10) NOT NULL,
	USERNAME VARCHAR2(100) PRIMARY KEY,
	PASSWORD VARCHAR2(255) NOT NULL,
	ROLEID NUMBER(5) NOT NULL,
	FAILEDLOGINATTEMPT NUMBER(3) DEFAULT 0,
	ACCOUNTLOCKED CHAR(1) DEFAULT 'N',
	CREATIONDATE TIMESTAMP,
	LASTSUCCESSFULLOGINDATE TIMESTAMP,
	LASTMODIFIEDDATE TIMESTAMP,
	MODIFIEDBY VARCHAR2(100),
	QUESTIONID NUMBER(5),
	SECURITYANSWER VARCHAR2(255)
);